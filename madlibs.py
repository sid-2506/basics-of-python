# Madlibs game
print("Welcome to the Madlibs game!")

adjective = input("Enter an adjective : ")
noun = input("Enter a noun : ")
adjective2 = input("Enter another adjective : ")
verb = input("Enter a verb : ")
adjective3 = input("Enter a third adjective : ")

print(f"Once upon a time, there was a {adjective} {noun}.")
print(f"It was a {adjective2} day, and the {noun} wanted to {verb}.")
print(f"Suddenly, a {adjective3} creature appeared!")
